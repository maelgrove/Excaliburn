namespace Excaliburn.ComponentModel.ToolBars
{
    /// <summary>
    ///     Represents the display mode of a toolbar command item.
    /// </summary>
    public enum ToolBarCommandItemDisplay
    {
        /// <summary>
        ///     Specifies that only the icon of the command should be displayed.
        /// </summary>
        IconOnly,

        /// <summary>
        ///     Specifies that the icon and tooltip of the command should be displayed.
        /// </summary>
        IconAndText
    }
}
