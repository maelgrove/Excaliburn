namespace Excaliburn.Modules.Workspace
{
    /// <summary>
    ///     Represents the alignment of a <see cref="IWorkspaceTool" /> within the workspace.
    /// </summary>
    public enum WorkspaceToolAlignment
    {
        /// <summary>
        ///     Specifies that the tool is aligned at the left of the workspace.
        /// </summary>
        Left,

        /// <summary>
        ///     Specifies that the tool is aligned at the top of the workspace.
        /// </summary>
        Top,

        /// <summary>
        ///     Specifies that the tool is aligned at the right of the workspace.
        /// </summary>
        Right,

        /// <summary>
        ///     Specifies that the tool is aligned at the bottom of the workspace.
        /// </summary>
        Bottom
    }
}
